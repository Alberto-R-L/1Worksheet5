

var edad = prompt("Escriba su edad");

if(edad <= 5){
    
    document.write("Usted deberia ir a jardin de infancia");

}   else    if(edad >=6 && edad <=11){

    document.write("Usted deberia ir a primaria ");

}   else    if(edad >=12 && edad <=16){

    document.write("Usted deberia ir a secundaria ");

}   else    if(edad >=17 && edad <=21){

    document.write("Usted deberia estar en bachillerato o en ciclos");

}
